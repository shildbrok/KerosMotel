    public void cadastrarAlug(){
       
       
        String numeroQuarto, clientes, funcionario1, funcionario2, tppagamento;
        numeroQuarto = txtNrm2.getText();
        clientes = "genivaldo";
        funcionario1 = txtNome.getText();
        funcionario2 = "";
        tppagamento = "";
        AluguelDTO alugDTO = new AluguelDTO();
        
        alugDTO.setNome(numeroQuarto);
        alugDTO.setFuncionario1(funcionario1);
        alugDTO.setTpPagamento(tppagamento);
        alugDTO.setFuncionario2(funcionario2);
        alugDTO.setClientes(clientes);
        
        AluguelDAO dao = new AluguelDAO();
        dao.cadastrarAluguel(alugDTO);
        
        Quartos q = new Quartos();
        QuartosDAO qDAO = new QuartosDAO();
        q.setStatus("ALUGADO");
        q.setNumeroQuarto(numeroQuarto);
        qDAO.mudarStatus(q);
        
        
        JPQrt.removeAll();
        JPQrt.revalidate();
        JPQrt.repaint();
        
        iniciarGridLayout();
    }
