    public void readJtable() {
        DefaultTableModel model = (DefaultTableModel) Jtable.getModel();
        model.setNumRows(0);
        ProdutoDAO daoP = new ProdutoDAO();

        for (Produto p : daoP.read()) {
            model.addRow(new Object[]{
                p.getId_produto(),
                p.getNome(),                
                p.getPrecoVenda(),
                p.getQtd(),
                p.getPrecoCompra()
            });
        }

    }

