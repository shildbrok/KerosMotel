    private void JtableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JtableMouseClicked
        // TODO add your handling code here:

        if (Jtable.getSelectedRow() != -1) {
            txtID.setText(Jtable.getValueAt(Jtable.getSelectedRow(), 0).toString());
            txtNome.setText(Jtable.getValueAt(Jtable.getSelectedRow(), 1).toString());
            txtPrcVenda.setText(Jtable.getValueAt(Jtable.getSelectedRow(), 2).toString().replace(".", ","));
            txtPrcCompra.setText(Jtable.getValueAt(Jtable.getSelectedRow(), 4).toString().replace(".", ","));
            txtQtd.setText(Jtable.getValueAt(Jtable.getSelectedRow(), 3).toString());
        }
    }//GEN-LAST:event_JtableMouseClicked

    private void btnAttActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAttActionPerformed
        // TODO add your handling code here:
        
        if (Jtable.getSelectedRow() != -1) {
        
        Produto prd = new Produto();
        ProdutoDAO p = new ProdutoDAO();

        prd.setNome(txtNome.getText());
        prd.setPrecoCompra(Float.parseFloat(txtPrcCompra.getText().replace(",", ".")));
        prd.setPrecoVenda(Float.parseFloat(txtPrcVenda.getText().replace(",", ".")));
        prd.setQtd(Integer.parseInt(txtQtd.getText()));
        prd.setId_produto((int) Jtable.getValueAt(Jtable.getSelectedRow(), 0));
        p.update(prd);


        readJtable();
        }
    }//GEN-LAST:event_btnAttActionPerformed

    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
        // TODO add your handling code here:

        if (Jtable.getSelectedRow() != -1) {

            
            Produto prd = new Produto();
            ProdutoDAO p = new ProdutoDAO();
            prd.setId_produto((int) Jtable.getValueAt(Jtable.getSelectedRow(), 0));
            p.delete(prd);

            readJtable();
        }

    }//GEN-LAST:event_btnDelActionPerformed

